#  5. IMPROVED AOC-IDS
# ══════════════════════════════════════════════════════
class ImprovedAOCIDS:
    def __init__(self, input_dim: int, cfg: Config):
        self.cfg          = cfg
        self.dev          = cfg.DEVICE
        self.model        = AttentionVAE(input_dim, cfg.LATENT_DIM).to(self.dev)
        self.opt          = torch.optim.Adam(self.model.parameters(), lr=cfg.LR)
        self.gp_en        = None   # Gaussian params (encoder)
        self.gp_de        = None   # Gaussian params (decoder)
        self.mean_n_en    = None   # mean normal rep — encoder
        self.mean_n_de    = None   # mean normal rep — decoder
        self._X0          = None   # kept for Gaussian refitting
        self._y0          = None

    # ── tensor helper ──────────────────────────────
    def _t(self, a): return torch.FloatTensor(a).to(self.dev)

    # ── extract representations ────────────────────
    def _reps(self, X: np.ndarray):
        self.model.eval()
        zs, rs = [], []
        with torch.no_grad():
            for (xb,) in DataLoader(TensorDataset(self._t(X)), batch_size=512):
                _, r, mu, _ = self.model(xb)
                zs.append(mu.cpu().numpy())
                rs.append(r.cpu().numpy())
        return np.vstack(zs), np.vstack(rs)

    # ── train one phase ────────────────────────────
    def _train(self, X: np.ndarray, y: np.ndarray, epochs: int):
        self.model.train()

        # IMPROVEMENT 4 — balanced sampler (fixes class imbalance)
        counts  = np.bincount(y)
        weights = 1.0 / counts[y]
        sampler = WeightedRandomSampler(weights, len(weights), replacement=True)
        ds      = TensorDataset(self._t(X), torch.LongTensor(y).to(self.dev))
        loader  = DataLoader(ds, batch_size=self.cfg.BATCH_SIZE, sampler=sampler)

        for _ in range(epochs):
            for xb, yb in loader:
                self.opt.zero_grad()
                z, r, mu, lv = self.model(xb)
                loss = (
                    crc_loss(mu, yb, self.cfg.TEMPERATURE) +   # encoder CRC
                    crc_loss(r,  yb, self.cfg.TEMPERATURE) +   # decoder CRC
                    self.cfg.KL_WEIGHT * kld_loss(mu, lv)  +   # VAE KLD
                    0.1 * F.mse_loss(r, xb)                    # reconstruction
                )
                loss.backward()
                torch.nn.utils.clip_grad_norm_(self.model.parameters(), 1.0)
                self.opt.step()

        # update mean normal representations
        z_all, r_all    = self._reps(X)
        self.mean_n_en  = z_all[y == 0].mean(0)
        self.mean_n_de  = r_all[y == 0].mean(0)

    # ── refit Gaussians on INITIAL data only ───────
    def _refit_gaussians(self):
        z, r = self._reps(self._X0)
        self.gp_en = fit_gaussians(cos_to_mean(z, self.mean_n_en))
        self.gp_de = fit_gaussians(cos_to_mean(r, self.mean_n_de))

    # ── predict + confidence ───────────────────────
    def _predict(self, X: np.ndarray):
        if self.gp_en is None:
            return np.zeros(len(X), dtype=int), np.ones(len(X))
        z, r     = self._reps(X)
        p_en, c_en = gaussian_decision(cos_to_mean(z, self.mean_n_en), self.gp_en)
        p_de, c_de = gaussian_decision(cos_to_mean(r, self.mean_n_de), self.gp_de)
        preds    = np.where(c_en >= c_de, p_en, p_de)   # voting
        confs    = np.maximum(c_en, c_de)
        return preds, confs

    # ── public API ─────────────────────────────────
    def fit_initial(self, X0: np.ndarray, y0: np.ndarray):
        print(f"  [Init] {len(X0)} samples, {self.cfg.EPOCH0} epochs ...")
        self._X0, self._y0 = X0.copy(), y0.copy()
        self._train(X0, y0, self.cfg.EPOCH0)
        self._refit_gaussians()

    def online_update(self, X_batch: np.ndarray,
                      X_full: np.ndarray, y_full: np.ndarray):
        """
        1. Pseudo-label incoming batch
        2. IMPROVEMENT 5 — keep only confident pseudo-labels
        3. Expand dataset & fine-tune
        """
        preds, confs = self._predict(X_batch)
        pseudo       = preds.copy()

        # reject low-confidence pseudo-labels (default → normal)
        pseudo[confs < self.cfg.CONFIDENCE_THR] = 0

        # same random-flip noise as base paper
        n_flip   = int(self.cfg.LAMBDA_FLIP * len(pseudo))
        flip_idx = np.random.choice(len(pseudo), size=n_flip, replace=False)
        pseudo[flip_idx] = 1 - pseudo[flip_idx]

        X_new = np.vstack([X_full,  X_batch])
        y_new = np.concatenate([y_full, pseudo])

        self._train(X_new, y_new, self.cfg.EPOCH1)
        self._refit_gaussians()           # always refit on clean initial data
        return preds, X_new, y_new

    def evaluate(self, X_test: np.ndarray, y_test: np.ndarray) -> dict:
        preds, _ = self._predict(X_test)
        return {
            "Acc" : accuracy_score(y_test, preds)                        * 100,
            "Pre" : precision_score(y_test, preds, zero_division=0)      * 100,
            "Rec" : recall_score(y_test, preds, zero_division=0)         * 100,
            "F1"  : f1_score(y_test, preds, zero_division=0)             * 100,
        }


# ══════════════════════════════════════════════════════
#  6. SINGLE RUN
# ══════════════════════════════════════════════════════
def run_once(X_train, y_train, X_test, y_test, cfg: Config, run_id: int) -> dict:
    np.random.seed(run_id * 42)
    torch.manual_seed(run_id * 42)

    # 20 % initial labeled, rest streams in
    X0, X_stream, y0, _ = train_test_split(
        X_train, y_train,
        test_size=(1 - cfg.INIT_RATIO),
        stratify=y_train,
        random_state=run_id,
    )

    model = ImprovedAOCIDS(X_train.shape[1], cfg)
    model.fit_initial(X0, y0)

    # evaluate after initial training only
    init_m = model.evaluate(X_test, y_test)
    print(f"  [After init ]  Acc={init_m['Acc']:.2f}%  F1={init_m['F1']:.2f}%")

    # online updates
    X_lab, y_lab = X0.copy(), y0.copy()
    n_batches    = len(X_stream) // cfg.UPDATE_EVERY

    for b in range(n_batches):
        s = b * cfg.UPDATE_EVERY
        _, X_lab, y_lab = model.online_update(
            X_stream[s : s + cfg.UPDATE_EVERY], X_lab, y_lab
        )
        if (b + 1) % 5 == 0 or b == n_batches - 1:
            m = model.evaluate(X_test, y_test)
            print(f"  [Batch {b+1:>3}/{n_batches}]  "
                  f"Acc={m['Acc']:.2f}%  F1={m['F1']:.2f}%")

    final = model.evaluate(X_test, y_test)
    print(f"  [Final      ]  Acc={final['Acc']:.2f}%  Pre={final['Pre']:.2f}%  "
          f"Rec={final['Rec']:.2f}%  F1={final['F1']:.2f}%")
    return final


