#  3. MODEL — Attention-VAE
# ══════════════════════════════════════════════════════
class AttentionGate(nn.Module):
    """
    IMPROVEMENT 1 — Feature-wise attention.
    Learns which of the 196 UNSW features matter most.
    """
    def __init__(self, d: int):
        super().__init__()
        self.gate = nn.Sequential(nn.Linear(d, d), nn.Sigmoid())
    def forward(self, x):
        return x * self.gate(x)


class VAEEncoder(nn.Module):
    """
    IMPROVEMENT 2 — Variational encoder.
    Outputs (μ, log σ²) instead of a single point,
    giving a smooth, generalizable latent space.
    """
    def __init__(self, in_d: int, latent_d: int):
        super().__init__()
        self.attn = AttentionGate(in_d)
        self.net  = nn.Sequential(
            nn.Linear(in_d, 128), nn.BatchNorm1d(128), nn.ReLU(),
            nn.Linear(128, 64),  nn.BatchNorm1d(64),  nn.ReLU(),
        )
        self.mu_fc = nn.Linear(64, latent_d)
        self.lv_fc = nn.Linear(64, latent_d)

    def forward(self, x):
        h = self.net(self.attn(x))
        return self.mu_fc(h), self.lv_fc(h)


class VAEDecoder(nn.Module):
    def __init__(self, latent_d: int, out_d: int):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(latent_d, 64),  nn.BatchNorm1d(64),  nn.ReLU(),
            nn.Linear(64, 128),       nn.BatchNorm1d(128), nn.ReLU(),
            nn.Linear(128, out_d),
        )
    def forward(self, z):
        return self.net(z)


class AttentionVAE(nn.Module):
    def __init__(self, in_d: int, latent_d: int):
        super().__init__()
        self.encoder = VAEEncoder(in_d, latent_d)
        self.decoder = VAEDecoder(latent_d, in_d)

    def reparameterise(self, mu, lv):
        if self.training:
            return mu + torch.randn_like(mu) * torch.exp(0.5 * lv)
        return mu          # use μ directly at inference (stable)

    def forward(self, x):
        mu, lv = self.encoder(x)
        z      = self.reparameterise(mu, lv)
        return z, self.decoder(z), mu, lv

    def decode(self, z):
        return self.decoder(z)


# ══════════════════════════════════════════════════════
#  4. LOSS FUNCTIONS
# ══════════════════════════════════════════════════════
def crc_loss(z: torch.Tensor, y: torch.Tensor, tau: float) -> torch.Tensor:
    """
    Cluster Repelling Contrastive loss (base paper).
    Applied to both encoder (μ) and decoder outputs.
    """
    nm = (y == 0); am = (y == 1)
    if nm.sum() < 2 or am.sum() < 1:
        return torch.tensor(0.0, device=z.device)
    zn = F.normalize(z[nm], dim=1)
    za = F.normalize(z[am], dim=1)
    total, cnt = torch.tensor(0.0, device=z.device), 0
    for i in range(len(zn)):
        a   = zn[i:i+1]
        pos = torch.cat([zn[:i], zn[i+1:]])
        if len(pos) == 0: continue
        for p in pos:
            num   = torch.exp((a @ p.unsqueeze(1)).squeeze() / tau)
            denom = num + torch.exp((a @ za.T) / tau).sum()
            total += -torch.log(num / (denom + 1e-8))
            cnt   += 1
    return total / max(cnt, 1)


def kld_loss(mu: torch.Tensor, lv: torch.Tensor) -> torch.Tensor:
    """
    IMPROVEMENT 3 — KL Divergence.
    Regularises latent space → better Gaussian decision boundaries.
    """
    return -0.5 * torch.mean(1 + lv - mu.pow(2) - lv.exp())


