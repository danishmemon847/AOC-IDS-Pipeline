#  MODELS
# ══════════════════════════════════════════════════════
class TeacherAE(nn.Module):
    """Base paper AE + Improvement 3 (feature dropout)."""
    def __init__(self, in_d, dropout=0.1):
        super().__init__()
        # encoder outputs 64-dim
        self.encoder = nn.Sequential(
            nn.Dropout(dropout),
            nn.Linear(in_d, 128), nn.BatchNorm1d(128), nn.ReLU(),
            nn.Linear(128, 64),  nn.BatchNorm1d(64),  nn.ReLU(),
        )
        self.decoder = nn.Sequential(
            nn.Linear(64, 128),  nn.BatchNorm1d(128), nn.ReLU(),
            nn.Linear(128, in_d),
        )

    def forward(self, x):
        z = self.encoder(x)
        return z, self.decoder(z)


class StudentAE(nn.Module):
    """
    Improvement 4: Lightweight AE — 32-dim encoder (half of teacher).
    Uses a projection head to align dimensions for distillation.
    """
    def __init__(self, in_d, teacher_enc_dim=64):
        super().__init__()
        self.enc_dim = 32
        # encoder outputs 32-dim
        self.encoder = nn.Sequential(
            nn.Linear(in_d, 64), nn.BatchNorm1d(64), nn.ReLU(),
            nn.Linear(64, 32),  nn.BatchNorm1d(32), nn.ReLU(),
        )
        self.decoder = nn.Sequential(
            nn.Linear(32, 64),  nn.BatchNorm1d(64), nn.ReLU(),
            nn.Linear(64, in_d),
        )
        # FIX: projection maps student 32-dim → teacher 64-dim for MSE loss
        self.proj = nn.Linear(32, teacher_enc_dim)

    def forward(self, x):
        z = self.encoder(x)
        return z, self.decoder(z)

    def forward_proj(self, x):
        """Returns (z, recon, projected_z_for_distillation)."""
        z     = self.encoder(x)
        recon = self.decoder(z)
        z_p   = self.proj(z)      # projected to teacher dim
        return z, recon, z_p


# ══════════════════════════════════════════════════════
#  IMPROVED AOC-IDS
# ══════════════════════════════════════════════════════
class ImprovedAOCIDS:
    def __init__(self, input_dim, cfg):
        self.cfg       = cfg
        self.dev       = cfg.DEVICE
        self.in_d      = input_dim

        self.teacher   = TeacherAE(input_dim, cfg.DROPOUT).to(self.dev)
        self.student   = StudentAE(input_dim, teacher_enc_dim=64).to(self.dev)
        self.opt_t     = torch.optim.Adam(self.teacher.parameters(), lr=cfg.LR)

        # after distillation, inference uses student
        self.use_student  = False
        self.gp_en = self.gp_de = None
        self.mean_n_en = self.mean_n_de = None
        self._X0 = self._y0 = None

    def _t(self, a): return torch.FloatTensor(a).to(self.dev)

    def _reps(self, X):
        """Always use teacher for Gaussian fitting and pseudo-labels."""
        self.teacher.eval()
        zs, rs = [], []
        with torch.no_grad():
            for (xb,) in DataLoader(TensorDataset(self._t(X)), batch_size=1024):
                z, r = self.teacher(xb)
                zs.append(z.cpu().numpy())
                rs.append(r.cpu().numpy())
        return np.vstack(zs), np.vstack(rs)

    def _reps_student(self, X):
        """Student representations for evaluation after distillation."""
        self.student.eval()
        zs, rs = [], []
        with torch.no_grad():
            for (xb,) in DataLoader(TensorDataset(self._t(X)), batch_size=1024):
                z, r, _ = self.student.forward_proj(xb)
                # use projected z so it's in same space as teacher
                zp = self.student.proj(z)
                zs.append(zp.cpu().numpy())
                rs.append(r.cpu().numpy())
        return np.vstack(zs), np.vstack(rs)

    # ── teacher training ───────────────────────────
    def _train_teacher(self, X, y, epochs):
        self.teacher.train()

        # IMPROVEMENT 1: SMOTE + weighted sampler
        X_bal, y_bal = smote_oversample(X, y)
        counts  = np.bincount(y_bal)
        weights = 1.0 / counts[y_bal]
        sampler = WeightedRandomSampler(weights, len(weights), replacement=True)
        ds      = TensorDataset(self._t(X_bal), torch.LongTensor(y_bal).to(self.dev))
        loader  = DataLoader(ds, batch_size=cfg.BATCH_SIZE, sampler=sampler)

        for ep in range(epochs):
            ep_loss = 0.0
            for xb, yb in loader:
                self.opt_t.zero_grad()
                z, r  = self.teacher(xb)
                loss  = crc_loss(z, yb, cfg.TEMPERATURE) + \
                        crc_loss(r, yb, cfg.TEMPERATURE)
                loss.backward()
                torch.nn.utils.clip_grad_norm_(self.teacher.parameters(), 1.0)
                self.opt_t.step()
                ep_loss += loss.item()
            if (ep+1) % 10 == 0:
                print(f"    epoch {ep+1}/{epochs}  loss={ep_loss/len(loader):.4f}")

        z_all, r_all   = self._reps(X)
        self.mean_n_en = z_all[y==0].mean(0)
        self.mean_n_de = r_all[y==0].mean(0)

    # ── knowledge distillation (Improvement 4) ─────
    def _distill_student(self, X, y):
        print("  [Distill] Training lightweight student...")
        self.teacher.eval()
        self.student.train()
        opt_s  = torch.optim.Adam(self.student.parameters(), lr=cfg.LR)
        loader = DataLoader(
            TensorDataset(self._t(X), torch.LongTensor(y).to(self.dev)),
            batch_size=256, shuffle=True)

        for ep in range(cfg.DISTILL_EPOCHS):
            for xb, yb in loader:
                opt_s.zero_grad()
                with torch.no_grad():
                    z_t, r_t = self.teacher(xb)   # teacher targets: 64-dim, in_d-dim

                z_s, r_s, z_p = self.student.forward_proj(xb)
                # z_p is 64-dim (projected) — matches teacher encoder dim
                # r_s is in_d-dim — matches teacher decoder dim

                loss = (F.mse_loss(z_p, z_t) +          # encoder alignment
                        F.mse_loss(r_s, r_t) +          # decoder alignment
                        crc_loss(z_p, yb, cfg.TEMPERATURE))  # CRC on student
                loss.backward()
                opt_s.step()

        print("  [Distill] Done")
        self.use_student = True

    def _refit_gaussians(self):
        z, r       = self._reps(self._X0)
        self.gp_en = fit_gaussians(cos_to_mean(z, self.mean_n_en))
        self.gp_de = fit_gaussians(cos_to_mean(r, self.mean_n_de))

    def _predict(self, X):
        if self.gp_en is None:
            dummy = np.zeros(len(X), dtype=int)
            return dummy, dummy, dummy

        z, r       = self._reps(X)
        p_en, c_en = gaussian_decision(cos_to_mean(z, self.mean_n_en), self.gp_en)
        p_de, c_de = gaussian_decision(cos_to_mean(r, self.mean_n_de), self.gp_de)
        preds      = np.where(c_en >= c_de, p_en, p_de)
        return preds, p_en, p_de

    # ── public API ─────────────────────────────────
    def fit_initial(self, X0, y0):
        self._X0, self._y0 = X0.copy(), y0.copy()
        print(f"  [Init] {len(X0)} samples | {cfg.EPOCH0} epochs")
        t0 = time.time()
        self._train_teacher(X0, y0, cfg.EPOCH0)
        self._refit_gaussians()
        print(f"  [Init] Done in {(time.time()-t0)/60:.1f} min")
        self._distill_student(X0, y0)   # Improvement 4
        self._refit_gaussians()

    def online_update(self, X_batch, X_full, y_full):
        preds, p_en, p_de = self._predict(X_batch)

        # IMPROVEMENT 2: only use pseudo-labels where enc & dec agree
        agree  = (p_en == p_de)
        pseudo = preds.copy()
        pseudo[~agree] = 0          # disagreements → default normal
        n_acc  = agree.sum()

        flip_idx = np.random.choice(len(pseudo),
                                    size=int(cfg.LAMBDA_FLIP * len(pseudo)),
                                    replace=False)
        pseudo[flip_idx] = 1 - pseudo[flip_idx]

        X_new = np.vstack([X_full, X_batch])
        y_new = np.concatenate([y_full, pseudo])
        self._train_teacher(X_new, y_new, cfg.EPOCH1)
        self._refit_gaussians()
        return preds, n_acc, X_new, y_new

    def evaluate(self, X_test, y_test):
        preds, _, _ = self._predict(X_test)
        return {
            "Acc": accuracy_score(y_test, preds)                   * 100,
            "Pre": precision_score(y_test, preds, zero_division=0) * 100,
            "Rec": recall_score(y_test, preds, zero_division=0)    * 100,
            "F1" : f1_score(y_test, preds, zero_division=0)        * 100,
        }

    def model_size(self):
        t = sum(p.numel() for p in self.teacher.parameters())
        s = sum(p.numel() for p in self.student.parameters())
        return t, s


