#  IMPROVED AOC-IDS
# ══════════════════════════════════════════════════════
class ImprovedAOCIDS:
    def __init__(self, input_dim: int, cfg: Config):
        self.cfg       = cfg
        self.dev       = cfg.DEVICE
        self.model     = BaseAE(input_dim).to(self.dev)
        self.opt       = torch.optim.SGD(           # EXACT same as paper
                            self.model.parameters(),
                            lr=cfg.LR)
        self.gp_en     = None
        self.gp_de     = None
        self.mean_n_en = None
        self.mean_n_de = None
        self._X0       = None
        self._y0       = None

    def _t(self, a):
        return torch.FloatTensor(a).to(self.dev)

    # ── representations ────────────────────────────
    def _reps(self, X: np.ndarray):
        self.model.eval()
        zs, rs = [], []
        with torch.no_grad():
            for (xb,) in DataLoader(
                    TensorDataset(self._t(X)), batch_size=1024):
                z, r = self.model(xb)
                zs.append(z.cpu().numpy())
                rs.append(r.cpu().numpy())
        return np.vstack(zs), np.vstack(rs)

    # ── training ───────────────────────────────────
    def _train(self, X: np.ndarray, y: np.ndarray, epochs: int):
        self.model.train()

        if self.cfg.USE_WEIGHTED_SAMPLER:
            # ── IMPROVEMENT 1: weighted sampler ────
            counts  = np.bincount(y)
            weights = 1.0 / counts[y]
            sampler = WeightedRandomSampler(
                weights, len(weights), replacement=True)
            ds     = TensorDataset(
                self._t(X), torch.LongTensor(y).to(self.dev))
            loader = DataLoader(
                ds, batch_size=self.cfg.BATCH_SIZE, sampler=sampler)
        else:
            # base paper: plain shuffle
            ds     = TensorDataset(
                self._t(X), torch.LongTensor(y).to(self.dev))
            loader = DataLoader(
                ds, batch_size=self.cfg.BATCH_SIZE, shuffle=True)

        for ep in range(epochs):
            ep_loss = 0.0
            for xb, yb in loader:
                self.opt.zero_grad()
                z, r  = self.model(xb)
                # exact base paper loss: CRC on encoder + CRC on decoder
                loss  = (crc_loss(z, yb, self.cfg.TEMPERATURE) +
                         crc_loss(r, yb, self.cfg.TEMPERATURE))
                loss.backward()
                self.opt.step()
                ep_loss += loss.item()
            if (ep + 1) % 50 == 0:
                print(f"    epoch {ep+1}/{epochs}  "
                      f"loss={ep_loss/len(loader):.4f}")

        # update mean normal rep on ORIGINAL (non-expanded) data
        z_all, r_all   = self._reps(X)
        self.mean_n_en = z_all[y == 0].mean(0)
        self.mean_n_de = r_all[y == 0].mean(0)

    # ── Gaussian fitting ───────────────────────────
    def _refit_gaussians(self):
        """Refit ONLY on clean initial data — same as base paper."""
        z, r       = self._reps(self._X0)
        self.gp_en = fit_gaussians(cos_to_mean(z, self.mean_n_en))
        self.gp_de = fit_gaussians(cos_to_mean(r, self.mean_n_de))

    # ── prediction ─────────────────────────────────
    def _predict(self, X: np.ndarray):
        if self.gp_en is None:
            dummy = np.zeros(len(X), dtype=int)
            return dummy, dummy, dummy, np.ones(len(X))

        z, r         = self._reps(X)
        p_en, c_en   = gaussian_decision(
            cos_to_mean(z, self.mean_n_en), self.gp_en)
        p_de, c_de   = gaussian_decision(
            cos_to_mean(r, self.mean_n_de), self.gp_de)

        # voting (same as base paper)
        preds = np.where(c_en >= c_de, p_en, p_de)
        confs = np.maximum(c_en, c_de)
        return preds, p_en, p_de, confs

    # ── public API ─────────────────────────────────
    def fit_initial(self, X0: np.ndarray, y0: np.ndarray):
        self._X0 = X0.copy()
        self._y0 = y0.copy()
        print(f"  [Init] {len(X0)} samples | {self.cfg.EPOCH0} epochs")
        t0 = time.time()
        self._train(X0, y0, self.cfg.EPOCH0)
        self._refit_gaussians()
        print(f"  [Init] Done in {(time.time()-t0)/60:.1f} min")

    def online_update(self, X_batch, X_full, y_full):
        preds, p_en, p_de, confs = self._predict(X_batch)

        if self.cfg.USE_AGREE_FILTER:
            # ── IMPROVEMENT 2: keep only agreed pseudo-labels ──
            agree          = (p_en == p_de)
            pseudo         = preds.copy()
            pseudo[~agree] = 0     # disagreed → safe default (normal)
            n_acc          = int(agree.sum())
        else:
            # base paper: use all pseudo-labels
            pseudo = preds.copy()
            n_acc  = len(pseudo)

        # random flip — exact same as base paper
        n_flip   = int(self.cfg.LAMBDA_FLIP * len(pseudo))
        flip_idx = np.random.choice(len(pseudo), size=n_flip, replace=False)
        pseudo[flip_idx] = 1 - pseudo[flip_idx]

        X_new = np.vstack([X_full,  X_batch])
        y_new = np.concatenate([y_full, pseudo])

        self._train(X_new, y_new, self.cfg.EPOCH1)
        self._refit_gaussians()
        return preds, n_acc, X_new, y_new

    def evaluate(self, X_test: np.ndarray, y_test: np.ndarray) -> dict:
        preds, _, _, _ = self._predict(X_test)
        return {
            "Acc": accuracy_score(y_test, preds)                   * 100,
            "Pre": precision_score(y_test, preds, zero_division=0) * 100,
            "Rec": recall_score(y_test, preds, zero_division=0)    * 100,
            "F1" : f1_score(y_test, preds, zero_division=0)        * 100,
        }


