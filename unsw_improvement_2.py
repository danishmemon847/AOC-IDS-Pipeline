    test_df  = pd.read_csv(cfg.TEST_PATH)

    print(f"[Data] Train columns : {list(train_df.columns[:6])} ...")
    print(f"[Data] Train shape   : {train_df.shape}")
    print(f"[Data] Test  shape   : {test_df.shape}")

    # ── find label column (case-insensitive) ────────
    label_col = next((c for c in train_df.columns if c.lower() == "label"), None)
    if label_col is None:
        raise ValueError(f"No 'label' column found. Columns: {list(train_df.columns)}")

    drop_cols = [c for c in ["id", "attack_cat", label_col]
                 if c in train_df.columns]

    y_train = train_df[label_col].values.astype(int)
