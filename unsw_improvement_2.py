def smote_oversample(X, y):
    classes, counts = np.unique(y, return_counts=True)
    min_class = classes[np.argmin(counts)]
    maj_count = counts.max()
    min_count = counts.min()

    X_min    = X[y == min_class]
    n_needed = maj_count - min_count
    synthetic = []

    for _ in range(n_needed):
        i, j  = np.random.choice(len(X_min), 2, replace=False)
        alpha = np.random.random()
        synthetic.append(X_min[i] * alpha + X_min[j] * (1 - alpha))

    X_new = np.vstack([X, np.array(synthetic)])
    y_new = np.concatenate([y, np.full(n_needed, min_class, dtype=int)])
    idx   = np.random.permutation(len(X_new))
    return X_new[idx], y_new[idx]


# ══════════════════════════════════════════════════════
#  GAUSSIAN UTILS
# ══════════════════════════════════════════════════════
def fit_gaussians(scores):
    scores = np.clip(scores, -1.0, 1.0)

    def nll(p):
        m1,s1,m2,s2,w = p
        s1=max(s1,1e-4); s2=max(s2,1e-4); w=np.clip(w,0.05,0.95)
        pdf = w*norm.pdf(scores,m1,s1) + (1-w)*norm.pdf(scores,m2,s2)
        return -np.sum(np.log(pdf + 1e-10))

    q25, q75 = np.percentile(scores, [25, 75])
    inits = [
        [q75, 0.1,  q25, 0.1,  0.5],
        [0.8, 0.1,  0.2, 0.2,  0.5],
        [0.6, 0.15, 0.1, 0.15, 0.5],
    ]
    best, bv = None, np.inf
    for x0 in inits:
        try:
            r = minimize(nll, x0,
                         bounds=[(-1,1),(1e-4,1),(-1,1),(1e-4,1),(0.05,0.95)],
                         method="L-BFGS-B")
            if r.fun < bv: bv, best = r.fun, r
        except: continue

    if best is None:
        med = np.median(scores)
        return (med, 0.15, med-0.4, 0.15, 0.5)
    return tuple(best.x)


def gaussian_decision(sims, gp):
    m1,s1,m2,s2,w = gp
    s1=max(s1,1e-4); s2=max(s2,1e-4)

    if abs(m1-m2) < 0.05:          # collapse guard
        thr   = np.median(sims)
        preds = (sims < thr).astype(int)
        confs = np.clip(np.abs(sims-thr)/(np.std(sims)+1e-8), 0, 1)
        return preds, confs

    if m1 >= m2:
        pn = w*norm.pdf(sims,m1,s1);     pa = (1-w)*norm.pdf(sims,m2,s2)
    else:
        pn = (1-w)*norm.pdf(sims,m2,s2); pa = w*norm.pdf(sims,m1,s1)

    total = pn + pa + 1e-10
    return (pa > pn).astype(int), np.maximum(pn,pa)/total


def cos_to_mean(reps, mean):
    nr = reps / (np.linalg.norm(reps, axis=1, keepdims=True)+1e-8)
    nm = mean / (np.linalg.norm(mean)+1e-8)
    return nr @ nm


# ══════════════════════════════════════════════════════
#  VECTORIZED CRC LOSS
# ══════════════════════════════════════════════════════
def crc_loss(z, y, tau):
    nm = (y==0); am = (y==1)
    if nm.sum() < 2 or am.sum() < 1:
        return torch.tensor(0.0, device=z.device, requires_grad=True)

    zn = F.normalize(z[nm], dim=1)
    za = F.normalize(z[am], dim=1)

    sim_nn = torch.mm(zn, zn.T) / tau
    sim_na = torch.mm(zn, za.T) / tau
    N      = zn.size(0)
    mask   = ~torch.eye(N, dtype=torch.bool, device=z.device)

    pos_sims    = sim_nn[mask].view(N, -1)
    denom_terms = torch.cat([sim_nn.masked_fill(~mask, -1e9), sim_na], dim=1)
    log_denom   = torch.logsumexp(denom_terms, dim=1, keepdim=True)
    return (-pos_sims + log_denom).mean()


