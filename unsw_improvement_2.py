class ImprovedAOCIDS:
    def __init__(self, input_dim, cfg):
        self.cfg       = cfg
        self.dev       = cfg.DEVICE
        self.model     = AttentionVAE(input_dim, cfg.LATENT_DIM).to(self.dev)
        self.opt       = torch.optim.Adam(self.model.parameters(), lr=cfg.LR)
        self.gp_en     = None
        self.gp_de     = None
        self.mean_n_en = None
        self.mean_n_de = None
        self._X0 = self._y0 = None

    def _t(self, a): return torch.FloatTensor(a).to(self.dev)

    def _reps(self, X):
        self.model.eval()
        zs, rs = [], []
        with torch.no_grad():
            for (xb,) in DataLoader(TensorDataset(self._t(X)), batch_size=1024):
                _, r, mu, _ = self.model(xb)
                zs.append(mu.cpu().numpy())
                rs.append(r.cpu().numpy())
        return np.vstack(zs), np.vstack(rs)

    def _train(self, X, y, epochs):
        self.model.train()
        counts  = np.bincount(y)
        weights = 1.0 / counts[y]
        sampler = WeightedRandomSampler(weights, len(weights), replacement=True)
        ds      = TensorDataset(self._t(X), torch.LongTensor(y).to(self.dev))
        loader  = DataLoader(ds, batch_size=self.cfg.BATCH_SIZE, sampler=sampler)

        for ep in range(epochs):
            ep_loss = 0.0
            for xb, yb in loader:
                self.opt.zero_grad()
                z, r, mu, lv = self.model(xb)
                loss = (
                    crc_loss_fast(mu, yb, self.cfg.TEMPERATURE) +
                    crc_loss_fast(r,  yb, self.cfg.TEMPERATURE) +
                    self.cfg.KL_WEIGHT * kld_loss(mu, lv) +
                    0.1 * F.mse_loss(r, xb)
                )
                loss.backward()
                torch.nn.utils.clip_grad_norm_(self.model.parameters(), 1.0)
                self.opt.step()
                ep_loss += loss.item()
            if (ep+1) % 10 == 0:
                print(f"    epoch {ep+1}/{epochs}  loss={ep_loss/len(loader):.4f}")

        z_all, r_all   = self._reps(X)
        self.mean_n_en = z_all[y==0].mean(0)
        self.mean_n_de = r_all[y==0].mean(0)

    def _refit_gaussians(self):
        z, r       = self._reps(self._X0)
        self.gp_en = fit_gaussians(cos_to_mean(z, self.mean_n_en))
        self.gp_de = fit_gaussians(cos_to_mean(r, self.mean_n_de))

    def _predict(self, X):
        if self.gp_en is None:
            return np.zeros(len(X), dtype=int), np.ones(len(X))
        z, r         = self._reps(X)
        p_en, c_en   = gaussian_decision(cos_to_mean(z, self.mean_n_en), self.gp_en)
        p_de, c_de   = gaussian_decision(cos_to_mean(r, self.mean_n_de), self.gp_de)
        preds        = np.where(c_en >= c_de, p_en, p_de)
        confs        = np.maximum(c_en, c_de)
        return preds, confs

    def fit_initial(self, X0, y0):
        self._X0, self._y0 = X0.copy(), y0.copy()
        print(f"  [Init] {len(X0)} samples | {self.cfg.EPOCH0} epochs")
        t0 = time.time()
        self._train(X0, y0, self.cfg.EPOCH0)
        self._refit_gaussians()
        print(f"  [Init] Done in {(time.time()-t0)/60:.1f} min")

    def online_update(self, X_batch, X_full, y_full):
        preds, confs = self._predict(X_batch)
        pseudo       = preds.copy()
        pseudo[confs < self.cfg.CONFIDENCE_THR] = 0
        flip_idx = np.random.choice(len(pseudo),
                                    size=int(self.cfg.LAMBDA_FLIP*len(pseudo)),
                                    replace=False)
        pseudo[flip_idx] = 1 - pseudo[flip_idx]
        X_new = np.vstack([X_full, X_batch])
        y_new = np.concatenate([y_full, pseudo])
        self._train(X_new, y_new, self.cfg.EPOCH1)
        self._refit_gaussians()
        return preds, X_new, y_new

    def evaluate(self, X_test, y_test):
        preds, _ = self._predict(X_test)
        return {
            "Acc": accuracy_score(y_test, preds)*100,
            "Pre": precision_score(y_test, preds, zero_division=0)*100,
            "Rec": recall_score(y_test, preds, zero_division=0)*100,
            "F1" : f1_score(y_test, preds, zero_division=0)*100,
        }


