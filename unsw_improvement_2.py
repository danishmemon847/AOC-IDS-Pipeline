#  2. GAUSSIAN UTILS  (same logic as base paper)
# ══════════════════════════════════════════════════════
def fit_gaussians(scores: np.ndarray) -> tuple:
    """MLE fit of two Gaussians to similarity scores."""
    def nll(p):
        m1, s1, m2, s2, w = p
        s1 = max(s1, 1e-6); s2 = max(s2, 1e-6); w = np.clip(w, 0.01, 0.99)
        pdf = w * norm.pdf(scores, m1, s1) + (1-w) * norm.pdf(scores, m2, s2)
        return -np.sum(np.log(pdf + 1e-10))

    best, best_val = None, np.inf
    for _ in range(10):
        x0 = [np.random.uniform(0.3, 0.7),  np.random.uniform(0.05, 0.3),
              np.random.uniform(-0.3, 0.3), np.random.uniform(0.05, 0.3),
              np.random.uniform(0.3, 0.7)]
        try:
            res = minimize(nll, x0,
                           bounds=[(-1,1),(1e-5,2),(-1,1),(1e-5,2),(0.01,0.99)],
                           method="L-BFGS-B")
            if res.fun < best_val:
                best_val, best = res.fun, res
        except Exception:
            continue
    if best is None:
        med = np.median(scores)
        return (med, 0.1, med-0.3, 0.1, 0.5)
    return tuple(best.x)


def gaussian_decision(sims: np.ndarray, gp: tuple):
    m1, s1, m2, s2, w = gp
    s1 = max(s1, 1e-6); s2 = max(s2, 1e-6)
    if m1 >= m2:                          # higher mean  → normal
        pn = w     * norm.pdf(sims, m1, s1)
        pa = (1-w) * norm.pdf(sims, m2, s2)
    else:
        pn = (1-w) * norm.pdf(sims, m2, s2)
        pa = w     * norm.pdf(sims, m1, s1)
    total = pn + pa + 1e-10
    return (pa > pn).astype(int), np.maximum(pn, pa) / total


def cos_to_mean(reps: np.ndarray, mean: np.ndarray) -> np.ndarray:
    nr = reps / (np.linalg.norm(reps, axis=1, keepdims=True) + 1e-8)
    nm = mean / (np.linalg.norm(mean) + 1e-8)
    return nr @ nm


