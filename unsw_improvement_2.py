# ══════════════════════════════════════════════════════
#  GAUSSIAN UTILS — FIX 2: stable decision making
# ══════════════════════════════════════════════════════
def fit_gaussians(scores: np.ndarray) -> tuple:
    """MLE fit of two Gaussians — with fallback for stability."""
    scores = np.clip(scores, -1.0, 1.0)   # clip to valid cosine range

    def nll(p):
        m1,s1,m2,s2,w = p
        s1=max(s1,1e-4); s2=max(s2,1e-4); w=np.clip(w,0.05,0.95)
        pdf = w*norm.pdf(scores,m1,s1) + (1-w)*norm.pdf(scores,m2,s2)
        return -np.sum(np.log(pdf + 1e-10))

    # smarter initialization using data quantiles
    q25, q75 = np.percentile(scores, 25), np.percentile(scores, 75)
    inits = [
        [q75, 0.1, q25, 0.1, 0.5],          # data-driven init
        [0.7,  0.1, 0.2, 0.1, 0.5],
        [0.5,  0.2, 0.0, 0.2, 0.6],
        [0.8,  0.05, 0.3, 0.15, 0.4],
    ]

    best, bv = None, np.inf
    for x0 in inits:
        try:
            r = minimize(nll, x0,
                         bounds=[(-1,1),(1e-4,1),(-1,1),(1e-4,1),(0.05,0.95)],
                         method="L-BFGS-B")
            if r.fun < bv:
                bv, best = r.fun, r
        except: continue

    if best is None:
        med = np.median(scores)
        return (med, 0.15, med-0.4, 0.15, 0.5)

    return tuple(best.x)


def gaussian_decision(sims: np.ndarray, gp: tuple):
    """
    FIX 2: Added sanity check — if Gaussians are too close
    (collapsed), fall back to median threshold to prevent
    the accuracy crash seen in later batches.
    """
    m1,s1,m2,s2,w = gp
    s1=max(s1,1e-4); s2=max(s2,1e-4)

    # sanity check: are the two Gaussians well-separated?
    separation = abs(m1 - m2)
    if separation < 0.05:
        # Gaussians collapsed — use simple median threshold
        threshold = np.median(sims)
        preds     = (sims < threshold).astype(int)  # below median = abnormal
        confs     = np.abs(sims - threshold) / (np.std(sims) + 1e-8)
        confs     = np.clip(confs, 0, 1)
        return preds, confs

    if m1 >= m2:
        pn = w     * norm.pdf(sims, m1, s1)
        pa = (1-w) * norm.pdf(sims, m2, s2)
    else:
        pn = (1-w) * norm.pdf(sims, m2, s2)
        pa = w     * norm.pdf(sims, m1, s1)

    total = pn + pa + 1e-10
    return (pa > pn).astype(int), np.maximum(pn, pa) / total


def cos_to_mean(reps: np.ndarray, mean: np.ndarray) -> np.ndarray:
    nr = reps / (np.linalg.norm(reps, axis=1, keepdims=True) + 1e-8)
    nm = mean / (np.linalg.norm(mean) + 1e-8)
    return nr @ nm


# ══════════════════════════════════════════════════════
#  VECTORIZED CRC LOSS
# ══════════════════════════════════════════════════════
def crc_loss_fast(z: torch.Tensor, y: torch.Tensor, tau: float) -> torch.Tensor:
    nm = (y == 0); am = (y == 1)
    if nm.sum() < 2 or am.sum() < 1:
        return torch.tensor(0.0, device=z.device, requires_grad=True)

    zn = F.normalize(z[nm], dim=1)
    za = F.normalize(z[am], dim=1)

    sim_nn = torch.mm(zn, zn.T) / tau           # (N, N)
    sim_na = torch.mm(zn, za.T) / tau           # (N, A)

    mask = ~torch.eye(zn.size(0), dtype=torch.bool, device=z.device)
    pos_sims = sim_nn[mask].view(zn.size(0), -1)  # (N, N-1)

    denom_terms = torch.cat([
        sim_nn.masked_fill(~mask, -1e9),
        sim_na
    ], dim=1)
    log_denom = torch.logsumexp(denom_terms, dim=1, keepdim=True)  # (N,1)

    return (-pos_sims + log_denom).mean()


def kld_loss(mu, lv):
    return -0.5 * torch.mean(1 + lv - mu.pow(2) - lv.exp())


# ══════════════════════════════════════════════════════
#  MODEL — Attention VAE
# ══════════════════════════════════════════════════════
class AttentionGate(nn.Module):
    def __init__(self, d):
        super().__init__()
        self.gate = nn.Sequential(nn.Linear(d, d), nn.Sigmoid())
    def forward(self, x): return x * self.gate(x)


class VAEEncoder(nn.Module):
    def __init__(self, in_d, latent_d):
        super().__init__()
        self.attn = AttentionGate(in_d)
        self.net  = nn.Sequential(
            nn.Linear(in_d, 128), nn.BatchNorm1d(128), nn.ReLU(),
            nn.Linear(128, 64),  nn.BatchNorm1d(64),  nn.ReLU(),
        )
        self.mu_fc = nn.Linear(64, latent_d)
        self.lv_fc = nn.Linear(64, latent_d)

    def forward(self, x):
        h = self.net(self.attn(x))
        return self.mu_fc(h), self.lv_fc(h)


class VAEDecoder(nn.Module):
    def __init__(self, latent_d, out_d):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(latent_d, 64),  nn.BatchNorm1d(64),  nn.ReLU(),
            nn.Linear(64, 128),       nn.BatchNorm1d(128), nn.ReLU(),
            nn.Linear(128, out_d),
        )
    def forward(self, z): return self.net(z)


class AttentionVAE(nn.Module):
    def __init__(self, in_d, latent_d):
        super().__init__()
        self.encoder = VAEEncoder(in_d, latent_d)
        self.decoder = VAEDecoder(latent_d, in_d)

    def reparameterise(self, mu, lv):
        if self.training:
            return mu + torch.randn_like(mu) * torch.exp(0.5 * lv)
        return mu

    def forward(self, x):
        mu, lv = self.encoder(x)
        z = self.reparameterise(mu, lv)
        return z, self.decoder(z), mu, lv

    def decode(self, z): return self.decoder(z)


# ══════════════════════════════════════════════════════
#  IMPROVED AOC-IDS
# ══════════════════════════════════════════════════════
class ImprovedAOCIDS:
    def __init__(self, input_dim, cfg):
        self.cfg       = cfg
        self.dev       = cfg.DEVICE
        self.model     = AttentionVAE(input_dim, cfg.LATENT_DIM).to(self.dev)
        self.opt       = torch.optim.Adam(self.model.parameters(), lr=cfg.LR)
        self.gp_en     = None
        self.gp_de     = None
        self.mean_n_en = None
        self.mean_n_de = None
        self._X0 = self._y0 = None

    def _t(self, a): return torch.FloatTensor(a).to(self.dev)

    def _reps(self, X):
        self.model.eval()
        zs, rs = [], []
        with torch.no_grad():
            for (xb,) in DataLoader(TensorDataset(self._t(X)), batch_size=1024):
                _, r, mu, _ = self.model(xb)
                zs.append(mu.cpu().numpy())
                rs.append(r.cpu().numpy())
        return np.vstack(zs), np.vstack(rs)

    def _train(self, X, y, epochs):
        self.model.train()

        # ── IMPROVEMENT 1: balanced sampler ─────────
        counts  = np.bincount(y)
        weights = 1.0 / counts[y]
        sampler = WeightedRandomSampler(weights, len(weights), replacement=True)
        ds      = TensorDataset(self._t(X), torch.LongTensor(y).to(self.dev))
        loader  = DataLoader(ds, batch_size=self.cfg.BATCH_SIZE, sampler=sampler)

        for ep in range(epochs):
            ep_loss = 0.0
            for xb, yb in loader:
                self.opt.zero_grad()
                z, r, mu, lv = self.model(xb)
                loss = (
                    crc_loss_fast(mu, yb, self.cfg.TEMPERATURE) +
                    crc_loss_fast(r,  yb, self.cfg.TEMPERATURE) +
                    self.cfg.KL_WEIGHT * kld_loss(mu, lv) +
                    0.1 * F.mse_loss(r, xb)
                )
                loss.backward()
                torch.nn.utils.clip_grad_norm_(self.model.parameters(), 1.0)
                self.opt.step()
                ep_loss += loss.item()
            if (ep+1) % 10 == 0:
                print(f"    epoch {ep+1}/{epochs}  loss={ep_loss/len(loader):.4f}")

        z_all, r_all   = self._reps(X)
        self.mean_n_en = z_all[y==0].mean(0)
        self.mean_n_de = r_all[y==0].mean(0)

    def _refit_gaussians(self):
        """Always refit on clean initial data only — prevents drift."""
        z, r       = self._reps(self._X0)
        self.gp_en = fit_gaussians(cos_to_mean(z, self.mean_n_en))
        self.gp_de = fit_gaussians(cos_to_mean(r, self.mean_n_de))

    def _predict(self, X):
        if self.gp_en is None:
            return np.zeros(len(X), dtype=int), np.ones(len(X))
        z, r       = self._reps(X)
        p_en, c_en = gaussian_decision(cos_to_mean(z, self.mean_n_en), self.gp_en)
        p_de, c_de = gaussian_decision(cos_to_mean(r, self.mean_n_de), self.gp_de)
        preds      = np.where(c_en >= c_de, p_en, p_de)
        confs      = np.maximum(c_en, c_de)
        return preds, confs

    def fit_initial(self, X0, y0):
        self._X0, self._y0 = X0.copy(), y0.copy()
        print(f"  [Init] {len(X0)} samples | {self.cfg.EPOCH0} epochs")
        t0 = time.time()
        self._train(X0, y0, self.cfg.EPOCH0)
        self._refit_gaussians()
        print(f"  [Init] Done in {(time.time()-t0)/60:.1f} min")

    def online_update(self, X_batch, X_full, y_full):
        preds, confs = self._predict(X_batch)
        pseudo       = preds.copy()

        # ── IMPROVEMENT 2: confidence filtering ─────
        low_conf             = confs < self.cfg.CONFIDENCE_THR
        pseudo[low_conf]     = 0    # reject uncertain → default normal
        n_accepted           = np.sum(~low_conf)

        # same random flip as base paper
        flip_idx = np.random.choice(len(pseudo),
                                    size=int(self.cfg.LAMBDA_FLIP * len(pseudo)),
                                    replace=False)
        pseudo[flip_idx] = 1 - pseudo[flip_idx]

        X_new = np.vstack([X_full, X_batch])
        y_new = np.concatenate([y_full, pseudo])
        self._train(X_new, y_new, self.cfg.EPOCH1)
        self._refit_gaussians()
        return preds, n_accepted, X_new, y_new

    def evaluate(self, X_test, y_test):
        preds, _ = self._predict(X_test)
        return {
            "Acc": accuracy_score(y_test, preds) * 100,
            "Pre": precision_score(y_test, preds, zero_division=0) * 100,
            "Rec": recall_score(y_test, preds, zero_division=0) * 100,
            "F1" : f1_score(y_test, preds, zero_division=0) * 100,
        }


