# ══════════════════════════════════════════════════════
#  BASE PAPER AE  — exact architecture
#  UNSW-NB15: [196, 128, 64, 128, 196]
# ══════════════════════════════════════════════════════
class BaseAE(nn.Module):
    def __init__(self, in_d: int):
        super().__init__()
        self.encoder = nn.Sequential(
            nn.Linear(in_d, 128), nn.ReLU(),
            nn.Linear(128, 64),   nn.ReLU(),
        )
        self.decoder = nn.Sequential(
            nn.Linear(64,  128),  nn.ReLU(),
            nn.Linear(128, in_d),
        )

    def forward(self, x):
        z = self.encoder(x)
        r = self.decoder(z)
        return z, r

    def encode(self, x): return self.encoder(x)
    def decode(self, z): return self.decoder(z)


# ══════════════════════════════════════════════════════
#  CRC LOSS  — vectorized (exact paper logic, fast)
# ══════════════════════════════════════════════════════
def crc_loss(z: torch.Tensor, y: torch.Tensor, tau: float) -> torch.Tensor:
    """
    Cluster Repelling Contrastive loss from base paper.
    Fully vectorized — no Python loops.
    """
    nm = (y == 0)
    am = (y == 1)
    if nm.sum() < 2 or am.sum() < 1:
        return torch.tensor(0.0, device=z.device, requires_grad=True)

    zn = F.normalize(z[nm], dim=1)   # (N, D)
    za = F.normalize(z[am], dim=1)   # (A, D)

    N        = zn.size(0)
    sim_nn   = torch.mm(zn, zn.T) / tau          # (N, N)
    sim_na   = torch.mm(zn, za.T) / tau          # (N, A)
    mask     = ~torch.eye(N, dtype=torch.bool, device=z.device)

    pos_sims    = sim_nn[mask].view(N, N - 1)
    denom       = torch.cat(
        [sim_nn.masked_fill(~mask, -1e9), sim_na], dim=1)
    log_denom   = torch.logsumexp(denom, dim=1, keepdim=True)

    return (-pos_sims + log_denom).mean()


# ══════════════════════════════════════════════════════
#  GAUSSIAN UTILS  — exact base paper logic
# ══════════════════════════════════════════════════════
def fit_gaussians(scores: np.ndarray) -> tuple:
    scores = np.clip(scores, -1.0, 1.0)

    def nll(p):
        m1, s1, m2, s2, w = p
        s1 = max(s1, 1e-4); s2 = max(s2, 1e-4)
        w  = np.clip(w, 0.05, 0.95)
        pdf = w * norm.pdf(scores, m1, s1) + \
              (1-w) * norm.pdf(scores, m2, s2)
        return -np.sum(np.log(pdf + 1e-10))

    q25, q75 = np.percentile(scores, [25, 75])
    inits = [
        [q75,  0.10, q25,  0.10, 0.50],
        [0.80, 0.10, 0.20, 0.20, 0.50],
        [0.60, 0.15, 0.00, 0.15, 0.50],
        [0.70, 0.05, 0.30, 0.15, 0.60],
    ]
    best, bv = None, np.inf
    for x0 in inits:
        try:
            r = minimize(nll, x0,
                         bounds=[(-1,1),(1e-4,1),(-1,1),(1e-4,1),(0.05,0.95)],
                         method="L-BFGS-B")
            if r.fun < bv:
                bv, best = r.fun, r
        except Exception:
            continue

    if best is None:
        med = np.median(scores)
        return (med, 0.15, med - 0.4, 0.15, 0.5)
    return tuple(best.x)


def gaussian_decision(sims: np.ndarray, gp: tuple):
    m1, s1, m2, s2, w = gp
    s1 = max(s1, 1e-4); s2 = max(s2, 1e-4)

    # collapse guard
    if abs(m1 - m2) < 0.05:
        thr   = np.median(sims)
        preds = (sims < thr).astype(int)
        confs = np.clip(
            np.abs(sims - thr) / (np.std(sims) + 1e-8), 0, 1)
        return preds, confs

    if m1 >= m2:
        pn = w       * norm.pdf(sims, m1, s1)
        pa = (1 - w) * norm.pdf(sims, m2, s2)
    else:
        pn = (1 - w) * norm.pdf(sims, m2, s2)
        pa = w       * norm.pdf(sims, m1, s1)

    total = pn + pa + 1e-10
    return (pa > pn).astype(int), np.maximum(pn, pa) / total


def cos_to_mean(reps: np.ndarray, mean: np.ndarray) -> np.ndarray:
    nr = reps / (np.linalg.norm(reps, axis=1, keepdims=True) + 1e-8)
    nm = mean / (np.linalg.norm(mean) + 1e-8)
    return nr @ nm


