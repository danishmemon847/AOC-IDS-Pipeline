def load_unsw(cfg):
    for p in [cfg.TRAIN_PATH, cfg.TEST_PATH]:
        if not os.path.exists(p):
            raise FileNotFoundError(f"Not found: {p}")
    train_df = pd.read_csv(cfg.TRAIN_PATH)
    test_df  = pd.read_csv(cfg.TEST_PATH)
    if train_df.shape[0] < test_df.shape[0]:
        print("[INFO] Swapping train/test files")
        train_df, test_df = test_df, train_df
    label_col = next(c for c in train_df.columns if c.lower() == "label")
    drop_cols = [c for c in ["id","attack_cat",label_col] if c in train_df.columns]
    y_train = train_df[label_col].values.astype(int)
    y_test  = test_df[label_col].values.astype(int)
    X_train = train_df.drop(columns=drop_cols, errors="ignore")
    X_test  = test_df.drop(columns=drop_cols, errors="ignore")
    cat_cols = X_train.select_dtypes(include=["object","category"]).columns.tolist()
    if cat_cols:
        X_train = pd.get_dummies(X_train, columns=cat_cols)
        X_test  = pd.get_dummies(X_test,  columns=cat_cols)
        X_train, X_test = X_train.align(X_test, join="left", axis=1, fill_value=0)
    zero_var = X_train.columns[X_train.nunique() <= 1]
    X_train  = X_train.drop(columns=zero_var)
    X_test   = X_test.reindex(columns=X_train.columns, fill_value=0)
    scaler   = StandardScaler()
    X_train  = scaler.fit_transform(X_train.astype(float))
    X_test   = scaler.transform(X_test.astype(float))
    print(f"[Data] Train {X_train.shape} | Normal={np.sum(y_train==0)} Attack={np.sum(y_train==1)}")
    print(f"[Data] Test  {X_test.shape}  | Normal={np.sum(y_test==0)}  Attack={np.sum(y_test==1)}")
    return X_train, y_train, X_test, y_test

def mixup(xb, yb, alpha):
    lam = np.random.beta(alpha, alpha)
    idx = torch.randperm(xb.size(0), device=xb.device)
    return lam*xb+(1-lam)*xb[idx], lam*yb.float()+(1-lam)*yb[idx].float()

class IDS_Net(nn.Module):
    def __init__(self, in_d, lightweight=False):
        super().__init__()
        h1 = 64 if lightweight else 128
        h2 = 32 if lightweight else 64
        self.encoder = nn.Sequential(
            nn.Linear(in_d,h1), nn.BatchNorm1d(h1), nn.ReLU(),
            nn.Linear(h1,  h2), nn.BatchNorm1d(h2), nn.ReLU())
        self.decoder = nn.Sequential(
            nn.Linear(h2,h1), nn.BatchNorm1d(h1), nn.ReLU(),
            nn.Linear(h1,in_d))
        self.clf_enc = nn.Linear(h2,   1)
        self.clf_dec = nn.Linear(in_d, 1)
    def forward(self, x):
        z=self.encoder(x); r=self.decoder(z)
        return z, r, self.clf_enc(z).squeeze(-1), self.clf_dec(r).squeeze(-1)
    def n_params(self): return sum(p.numel() for p in self.parameters())

class ImprovedAOCIDS:
    def __init__(self, input_dim, cfg):
        self.cfg   = cfg
        self.dev   = cfg.DEVICE
        self.model = IDS_Net(input_dim, lightweight=cfg.USE_LIGHTWEIGHT).to(self.dev)
        self.opt   = torch.optim.Adam(self.model.parameters(), lr=cfg.LR, weight_decay=1e-4)
        self.sched = torch.optim.lr_scheduler.CosineAnnealingLR(self.opt, T_max=cfg.EPOCH0, eta_min=1e-5)

    def _t(self, a): return torch.FloatTensor(a).to(self.dev)

    def _loader(self, X, y):
        ds = TensorDataset(self._t(X), torch.LongTensor(y).to(self.dev))
        if self.cfg.USE_WEIGHTED_SAMPLER:
            counts  = np.bincount(y)
            weights = 1.0/counts[y]
            sampler = WeightedRandomSampler(weights, len(weights), replacement=True)
            return DataLoader(ds, batch_size=self.cfg.BATCH_SIZE, sampler=sampler)
        return DataLoader(ds, batch_size=self.cfg.BATCH_SIZE, shuffle=True)

    def _train(self, X, y, epochs, use_sched=False):
        self.model.train()
        loader = self._loader(X, y)
        for ep in range(epochs):
            tot = 0.0
            for xb, yb in loader:
                if self.cfg.USE_MIXUP:
                    xb, yb_s = mixup(xb, yb, self.cfg.MIXUP_ALPHA)
                else:
                    yb_s = yb.float()
                self.opt.zero_grad()
                _, r, le, ld = self.model(xb)
                loss = (F.binary_cross_entropy_with_logits(le, yb_s) +
                        F.binary_cross_entropy_with_logits(ld, yb_s) +
                        0.1*F.mse_loss(r, xb))
                loss.backward()
                torch.nn.utils.clip_grad_norm_(self.model.parameters(), 1.0)
                self.opt.step()
                tot += loss.item()
            if use_sched: self.sched.step()
            if (ep+1)%10==0:
                print(f"    epoch {ep+1}/{epochs}  loss={tot/len(loader):.4f}")

    def _predict(self, X):
        self.model.eval()
        le_all, ld_all = [], []
        with torch.no_grad():
            for (xb,) in DataLoader(TensorDataset(self._t(X)), batch_size=1024):
                _,_,le,ld = self.model(xb)
                le_all.append(le.cpu().numpy())
                ld_all.append(ld.cpu().numpy())
        le = np.concatenate(le_all); ld = np.concatenate(ld_all)
        prob_en = 1/(1+np.exp(-le)); prob_de = 1/(1+np.exp(-ld))
        pred_en = (prob_en>0.5).astype(int); pred_de = (prob_de>0.5).astype(int)
        conf_en = np.abs(prob_en-0.5)*2;     conf_de = np.abs(prob_de-0.5)*2
        preds   = np.where(conf_en>=conf_de, pred_en, pred_de)
        confs   = np.maximum(conf_en, conf_de)
        return preds, pred_en, pred_de, confs

    def fit_initial(self, X0, y0):
        print(f"  [Init] {len(X0)} samples | {self.cfg.EPOCH0} epochs | params={self.model.n_params():,}")
        t0=time.time()
        self._train(X0, y0, self.cfg.EPOCH0, use_sched=True)
        print(f"  [Init] Done in {(time.time()-t0)/60:.1f} min")

    def online_update(self, X_batch, X_full, y_full):
        preds, pred_en, pred_de, confs = self._predict(X_batch)
        high_conf = confs >= self.cfg.CONFIDENCE_THR
        agree     = (pred_en==pred_de) if self.cfg.USE_AGREE_FILTER else np.ones(len(preds),bool)
        accept    = high_conf & agree
        pseudo    = preds.copy(); pseudo[~accept]=0
        n_acc     = int(accept.sum())
        flip_idx  = np.random.choice(len(pseudo), size=int(self.cfg.LAMBDA_FLIP*len(pseudo)), replace=False)
        pseudo[flip_idx] = 1-pseudo[flip_idx]
        X_new = np.vstack([X_full, X_batch])
        y_new = np.concatenate([y_full, pseudo])
        self._train(X_new, y_new, self.cfg.EPOCH1)
        return preds, n_acc, X_new, y_new

    def evaluate(self, X_test, y_test):
        preds,_,_,_ = self._predict(X_test)
        return {"Acc": accuracy_score(y_test,preds)*100,
                "Pre": precision_score(y_test,preds,zero_division=0)*100,
                "Rec": recall_score(y_test,preds,zero_division=0)*100,
                "F1" : f1_score(y_test,preds,zero_division=0)*100}

BASE_PAPER = {"Acc":89.19,"Pre":90.65,"Rec":89.70,"F1":90.14}

def run_once(X_train, y_train, X_test, y_test, cfg, run_id):
    np.random.seed(run_id*42); torch.manual_seed(run_id*42)
    X0,X_stream,y0,_ = train_test_split(X_train,y_train,
        test_size=(1-cfg.INIT_RATIO),stratify=y_train,random_state=run_id)
    model = ImprovedAOCIDS(X_train.shape[1], cfg)
    model.fit_initial(X0, y0)
    m = model.evaluate(X_test, y_test)
    print(f"  [After init]  Acc={m['Acc']:.2f}%  F1={m['F1']:.2f}%")
    X_lab,y_lab = X0.copy(),y0.copy()
    n_batches   = len(X_stream)//cfg.UPDATE_EVERY
    best_acc    = m['Acc']; best_m=m.copy()
    for b in range(n_batches):
        s=b*cfg.UPDATE_EVERY; t0=time.time()
        _,n_acc,X_lab,y_lab = model.online_update(X_stream[s:s+cfg.UPDATE_EVERY],X_lab,y_lab)
        m = model.evaluate(X_test,y_test)
        if m['Acc']>best_acc: best_acc=m['Acc']; best_m=m.copy()
        print(f"  [Batch {b+1:>3}/{n_batches}]  Acc={m['Acc']:.2f}%  F1={m['F1']:.2f}%  "
              f"Accepted={n_acc}/{cfg.UPDATE_EVERY}  ({time.time()-t0:.0f}s)")
    final=model.evaluate(X_test,y_test)
    print(f"\n  ★ Final  Acc={final['Acc']:.2f}%  Pre={final['Pre']:.2f}%  Rec={final['Rec']:.2f}%  F1={final['F1']:.2f}%")
    print(f"  ★ Best   Acc={best_m['Acc']:.2f}%  F1={best_m['F1']:.2f}%")
    return final, best_m

