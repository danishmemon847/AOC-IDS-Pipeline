class AttentionGate(nn.Module):
    def __init__(self, d):
        super().__init__()
        self.gate = nn.Sequential(nn.Linear(d, d), nn.Sigmoid())
    def forward(self, x): return x * self.gate(x)


class VAEEncoder(nn.Module):
    def __init__(self, in_d, latent_d):
        super().__init__()
        self.attn = AttentionGate(in_d)
        self.net  = nn.Sequential(
            nn.Linear(in_d, 128), nn.BatchNorm1d(128), nn.ReLU(),
            nn.Linear(128, 64),  nn.BatchNorm1d(64),  nn.ReLU(),
        )
        self.mu_fc = nn.Linear(64, latent_d)
        self.lv_fc = nn.Linear(64, latent_d)

    def forward(self, x):
        h = self.net(self.attn(x))
        return self.mu_fc(h), self.lv_fc(h)


class VAEDecoder(nn.Module):
    def __init__(self, latent_d, out_d):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(latent_d, 64),  nn.BatchNorm1d(64),  nn.ReLU(),
            nn.Linear(64, 128),       nn.BatchNorm1d(128), nn.ReLU(),
            nn.Linear(128, out_d),
        )
    def forward(self, z): return self.net(z)


class AttentionVAE(nn.Module):
    def __init__(self, in_d, latent_d):
        super().__init__()
        self.encoder = VAEEncoder(in_d, latent_d)
        self.decoder = VAEDecoder(latent_d, in_d)

    def reparameterise(self, mu, lv):
        if self.training:
            return mu + torch.randn_like(mu) * torch.exp(0.5 * lv)
        return mu

    def forward(self, x):
        mu, lv = self.encoder(x)
        z      = self.reparameterise(mu, lv)
        return z, self.decoder(z), mu, lv

    def decode(self, z): return self.decoder(z)


def kld_loss(mu, lv):
    return -0.5 * torch.mean(1 + lv - mu.pow(2) - lv.exp())


