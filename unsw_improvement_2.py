def load_unsw(cfg):
    for p in [cfg.TRAIN_PATH, cfg.TEST_PATH]:
        if not os.path.exists(p):
            raise FileNotFoundError(f"Not found: {p}")

    train_df = pd.read_csv(cfg.TRAIN_PATH)
    test_df  = pd.read_csv(cfg.TEST_PATH)

    if train_df.shape[0] < test_df.shape[0]:
        print("[INFO] Swapping train/test files")
        train_df, test_df = test_df, train_df

    label_col = next((c for c in train_df.columns
                      if c.lower() == "label"), None)
    drop_cols = [c for c in ["id", "attack_cat", label_col]
                 if c in train_df.columns]

    y_train = train_df[label_col].values.astype(int)
    y_test  = test_df[label_col].values.astype(int)
    X_train = train_df.drop(columns=drop_cols, errors="ignore")
    X_test  = test_df.drop(columns=drop_cols, errors="ignore")

    cat_cols = X_train.select_dtypes(
        include=["object", "category"]).columns.tolist()
    if cat_cols:
        X_train = pd.get_dummies(X_train, columns=cat_cols)
        X_test  = pd.get_dummies(X_test,  columns=cat_cols)
        X_train, X_test = X_train.align(
            X_test, join="left", axis=1, fill_value=0)

    zero_var = X_train.columns[X_train.nunique() <= 1]
    X_train  = X_train.drop(columns=zero_var)
    X_test   = X_test.reindex(columns=X_train.columns, fill_value=0)

    scaler  = StandardScaler()
    X_train = scaler.fit_transform(X_train.astype(float))
    X_test  = scaler.transform(X_test.astype(float))

    print(f"[Data] Train {X_train.shape} | "
          f"Normal={np.sum(y_train==0)}  Attack={np.sum(y_train==1)}")
    print(f"[Data] Test  {X_test.shape}  | "
          f"Normal={np.sum(y_test==0)}   Attack={np.sum(y_test==1)}")
    return X_train, y_train, X_test, y_test


# ══════════════════════════════════════════════════════
#  AE — BatchNorm added for stable representations
# ══════════════════════════════════════════════════════
class AE(nn.Module):
    """
    Same structure as base paper [in→128→64→128→in]
    + BatchNorm for stable training (key fix for 78% issue).
    """
    def __init__(self, in_d: int):
        super().__init__()
        self.encoder = nn.Sequential(
            nn.Linear(in_d, 128),
            nn.BatchNorm1d(128),
            nn.ReLU(),
            nn.Linear(128, 64),
            nn.BatchNorm1d(64),
            nn.ReLU(),
        )
        self.decoder = nn.Sequential(
            nn.Linear(64,  128),
            nn.BatchNorm1d(128),
            nn.ReLU(),
            nn.Linear(128, in_d),
        )

    def forward(self, x):
        z = self.encoder(x)
        r = self.decoder(z)
        return z, r


# ══════════════════════════════════════════════════════
#  CRC LOSS — vectorized
# ══════════════════════════════════════════════════════
def crc_loss(z: torch.Tensor,
             y: torch.Tensor,
             tau: float) -> torch.Tensor:
    nm = (y == 0);  am = (y == 1)
    if nm.sum() < 2 or am.sum() < 1:
        return torch.tensor(0.0, device=z.device, requires_grad=True)

    zn = F.normalize(z[nm], dim=1)
    za = F.normalize(z[am], dim=1)
    N  = zn.size(0)

    sim_nn = torch.mm(zn, zn.T) / tau
    sim_na = torch.mm(zn, za.T) / tau
    mask   = ~torch.eye(N, dtype=torch.bool, device=z.device)

    pos_sims  = sim_nn[mask].view(N, N - 1)
    denom     = torch.cat(
        [sim_nn.masked_fill(~mask, -1e9), sim_na], dim=1)
    log_denom = torch.logsumexp(denom, dim=1, keepdim=True)

    return (-pos_sims + log_denom).mean()


# ══════════════════════════════════════════════════════
#  GAUSSIAN UTILS
# ══════════════════════════════════════════════════════
def fit_gaussians(scores: np.ndarray) -> tuple:
    scores = np.clip(scores, -1.0, 1.0)

    def nll(p):
        m1, s1, m2, s2, w = p
        s1 = max(s1, 1e-4);  s2 = max(s2, 1e-4)
        w  = np.clip(w, 0.05, 0.95)
        pdf = (w * norm.pdf(scores, m1, s1) +
               (1 - w) * norm.pdf(scores, m2, s2))
        return -np.sum(np.log(pdf + 1e-10))

    q10, q25, q75, q90 = np.percentile(scores, [10, 25, 75, 90])
    inits = [
        [q90,  0.05, q10,  0.05, 0.50],
        [q75,  0.10, q25,  0.10, 0.50],
        [0.80, 0.10, 0.20, 0.20, 0.50],
        [0.70, 0.05, 0.30, 0.10, 0.60],
        [0.60, 0.15, 0.00, 0.15, 0.50],
    ]
    best, bv = None, np.inf
    for x0 in inits:
        try:
            r = minimize(nll, x0,
                         bounds=[(-1,1),(1e-4,0.5),
                                 (-1,1),(1e-4,0.5),
                                 (0.05,0.95)],
                         method="L-BFGS-B")
            if r.fun < bv:
                bv, best = r.fun, r
        except Exception:
            continue

    if best is None:
        med = np.median(scores)
        return (med, 0.15, med - 0.4, 0.15, 0.5)
    return tuple(best.x)


def gaussian_decision(sims: np.ndarray, gp: tuple):
    m1, s1, m2, s2, w = gp
    s1 = max(s1, 1e-4);  s2 = max(s2, 1e-4)

    # collapse guard
    if abs(m1 - m2) < 0.05:
        thr   = np.median(sims)
        preds = (sims < thr).astype(int)
        confs = np.clip(
            np.abs(sims - thr) / (np.std(sims) + 1e-8), 0, 1)
        return preds, confs

    if m1 >= m2:
        pn = w       * norm.pdf(sims, m1, s1)
        pa = (1 - w) * norm.pdf(sims, m2, s2)
    else:
        pn = (1 - w) * norm.pdf(sims, m2, s2)
        pa = w       * norm.pdf(sims, m1, s1)

    total = pn + pa + 1e-10
    return (pa > pn).astype(int), np.maximum(pn, pa) / total


def cos_to_mean(reps: np.ndarray, mean: np.ndarray) -> np.ndarray:
    nr = reps / (np.linalg.norm(reps, axis=1, keepdims=True) + 1e-8)
    nm = mean / (np.linalg.norm(mean) + 1e-8)
    return nr @ nm


# ══════════════════════════════════════════════════════
#  IMPROVED AOC-IDS
# ══════════════════════════════════════════════════════
class ImprovedAOCIDS:
    def __init__(self, input_dim: int, cfg: Config):
        self.cfg       = cfg
        self.dev       = cfg.DEVICE
        self.model     = AE(input_dim).to(self.dev)
        # FIX: Adam converges much better than SGD here
        self.opt       = torch.optim.Adam(
                            self.model.parameters(), lr=cfg.LR)
        self.gp_en     = None
        self.gp_de     = None
        self.mean_n_en = None
        self.mean_n_de = None
        self._X0       = None
        self._y0       = None

    def _t(self, a):
        return torch.FloatTensor(a).to(self.dev)

    def _loader(self, X, y, weighted=False):
        ds = TensorDataset(
            self._t(X), torch.LongTensor(y).to(self.dev))
        if weighted and self.cfg.USE_WEIGHTED_SAMPLER:
            counts  = np.bincount(y)
            weights = 1.0 / counts[y]
            sampler = WeightedRandomSampler(
                weights, len(weights), replacement=True)
            return DataLoader(
                ds, batch_size=self.cfg.BATCH_SIZE, sampler=sampler)
        return DataLoader(
            ds, batch_size=self.cfg.BATCH_SIZE, shuffle=True)

    def _reps(self, X: np.ndarray):
        self.model.eval()
        zs, rs = [], []
        with torch.no_grad():
            for (xb,) in DataLoader(
                    TensorDataset(self._t(X)), batch_size=1024):
                z, r = self.model(xb)
                zs.append(z.cpu().numpy())
                rs.append(r.cpu().numpy())
        return np.vstack(zs), np.vstack(rs)

    # ── Phase 1: reconstruction warmup ─────────────
    def _warmup(self, X: np.ndarray, y: np.ndarray):
        """
        FIX: Train AE on reconstruction loss first.
        This initializes the encoder to a sensible representation
        space BEFORE CRC loss is applied.
        Without this, CRC starts from random features → poor separation.
        """
        print(f"    [Warmup] {self.cfg.WARMUP_EPOCHS} epochs "
              f"(reconstruction)...")
        self.model.train()
        loader = self._loader(X, y, weighted=False)

        for ep in range(self.cfg.WARMUP_EPOCHS):
            for xb, _ in loader:
                self.opt.zero_grad()
                _, r  = self.model(xb)
                loss  = F.mse_loss(r, xb)
                loss.backward()
                self.opt.step()

    # ── Phase 2: CRC training ───────────────────────
    def _train_crc(self, X: np.ndarray, y: np.ndarray,
                   epochs: int):
        self.model.train()
        # IMPROVEMENT 1: weighted sampler
        loader = self._loader(X, y, weighted=True)

        for ep in range(epochs):
            ep_loss = 0.0
            for xb, yb in loader:
                self.opt.zero_grad()
                z, r  = self.model(xb)
                loss  = (crc_loss(z, yb, self.cfg.TEMPERATURE) +
                         crc_loss(r, yb, self.cfg.TEMPERATURE))
                loss.backward()
                torch.nn.utils.clip_grad_norm_(
                    self.model.parameters(), 1.0)
                self.opt.step()
                ep_loss += loss.item()
            if (ep + 1) % 30 == 0:
                print(f"    epoch {ep+1}/{epochs}  "
                      f"loss={ep_loss/len(loader):.4f}")

        # FIX: mean normal rep from REAL normal samples only
        # (not oversampled/pseudo-labeled data)
        X_real_normal = X[y == 0]
        z_n, r_n       = self._reps(X_real_normal)
        self.mean_n_en = z_n.mean(0)
        self.mean_n_de = r_n.mean(0)

    def _refit_gaussians(self):
        """Always refit on clean initial labeled data."""
        z, r       = self._reps(self._X0)
        self.gp_en = fit_gaussians(
            cos_to_mean(z, self.mean_n_en))
        self.gp_de = fit_gaussians(
            cos_to_mean(r, self.mean_n_de))

    def _predict(self, X: np.ndarray):
        if self.gp_en is None:
            dummy = np.zeros(len(X), dtype=int)
            return dummy, dummy, dummy, np.ones(len(X))

        z, r         = self._reps(X)
        p_en, c_en   = gaussian_decision(
            cos_to_mean(z, self.mean_n_en), self.gp_en)
        p_de, c_de   = gaussian_decision(
            cos_to_mean(r, self.mean_n_de), self.gp_de)

        preds = np.where(c_en >= c_de, p_en, p_de)
        confs = np.maximum(c_en, c_de)
        return preds, p_en, p_de, confs

    # ── public API ─────────────────────────────────
    def fit_initial(self, X0: np.ndarray, y0: np.ndarray):
        self._X0 = X0.copy()
        self._y0 = y0.copy()
        print(f"  [Init] {len(X0)} samples | "
              f"warmup={self.cfg.WARMUP_EPOCHS} + "
              f"CRC={self.cfg.EPOCH0} epochs")
        t0 = time.time()

        self._warmup(X0, y0)           # Phase 1
        self._train_crc(X0, y0,        # Phase 2
                        self.cfg.EPOCH0)
        self._refit_gaussians()
        print(f"  [Init] Done in {(time.time()-t0)/60:.1f} min")

    def online_update(self, X_batch, X_full, y_full):
        preds, p_en, p_de, confs = self._predict(X_batch)

        # IMPROVEMENT 2: agreement filter
        if self.cfg.USE_AGREE_FILTER:
            agree          = (p_en == p_de)
            pseudo         = preds.copy()
            pseudo[~agree] = 0
            n_acc          = int(agree.sum())
        else:
            pseudo = preds.copy()
            n_acc  = len(pseudo)

        # random flip same as base paper
        n_flip   = int(self.cfg.LAMBDA_FLIP * len(pseudo))
        flip_idx = np.random.choice(
            len(pseudo), size=n_flip, replace=False)
        pseudo[flip_idx] = 1 - pseudo[flip_idx]

        X_new = np.vstack([X_full,  X_batch])
        y_new = np.concatenate([y_full, pseudo])

        self._train_crc(X_new, y_new, self.cfg.EPOCH1)
        self._refit_gaussians()
        return preds, n_acc, X_new, y_new

    def evaluate(self, X_test: np.ndarray,
                 y_test: np.ndarray) -> dict:
        preds, _, _, _ = self._predict(X_test)
        return {
            "Acc": accuracy_score(y_test, preds)                   * 100,
            "Pre": precision_score(y_test, preds, zero_division=0) * 100,
            "Rec": recall_score(y_test, preds, zero_division=0)    * 100,
            "F1" : f1_score(y_test, preds, zero_division=0)        * 100,
        }


