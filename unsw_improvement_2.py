def run_once(X_train, y_train, X_test, y_test, cfg, run_id):
    np.random.seed(run_id * 42)
    torch.manual_seed(run_id * 42)

    X0, X_stream, y0, _ = train_test_split(
        X_train, y_train,
        test_size=(1 - cfg.INIT_RATIO),
        stratify=y_train,
        random_state=run_id,
    )

    model = ImprovedAOCIDS(X_train.shape[1], cfg)
    model.fit_initial(X0, y0)

    m = model.evaluate(X_test, y_test)
    print(f"  [After init]  Acc={m['Acc']:.2f}%  F1={m['F1']:.2f}%")

    X_lab, y_lab = X0.copy(), y0.copy()
    n_batches    = len(X_stream) // cfg.UPDATE_EVERY

    for b in range(n_batches):
        s  = b * cfg.UPDATE_EVERY
        t0 = time.time()
        _, n_acc, X_lab, y_lab = model.online_update(
            X_stream[s:s + cfg.UPDATE_EVERY], X_lab, y_lab)
        m = model.evaluate(X_test, y_test)
        print(f"  [Batch {b+1:>3}/{n_batches}]  "
              f"Acc={m['Acc']:.2f}%  F1={m['F1']:.2f}%  "
              f"Agreed={n_acc}/{cfg.UPDATE_EVERY}  "
              f"({time.time()-t0:.0f}s)")

    final = model.evaluate(X_test, y_test)
    print(f"\n  ★ Final  Acc={final['Acc']:.2f}%  "
          f"Pre={final['Pre']:.2f}%  "
          f"Rec={final['Rec']:.2f}%  "
          f"F1={final['F1']:.2f}%")
    return final


